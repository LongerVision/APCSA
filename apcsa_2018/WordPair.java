package apcsa_2018;

public class WordPair {
    private String firstS;
    private String secondS;

    public WordPair(String first, String second) {
        this.firstS = first;
        this.secondS = second;
    }

    public String getFirst() {
        return firstS;
    }

    public String getSecond() {
        return secondS;
    }
}
